                <h1>Etapas do Registro de Marca</h1>
                <hr />
                <p>
                    • BUSCA PRÉVIA – A busca prévia não é obrigatória, entretanto, 
                    é aconselhável ao interessado realizá-la antes de efetuar o depósito, 
                    com o intuito de verificar se já existe MARCA anteriormente 
                    depositada / registrada.</br></br> 

                    • DEPÓSITO – O pedido de registro de MARCA é requerido através de formulário 
                    próprio, no qual são prestadas informações e fornecidos dados sobre a MARCA e o 
                    requerente. Devem constar ainda do requerimento as etiquetas das MARCAS, 
                    quando for o caso, e o comprovante do pagamento da retribuição ao depósito.</br></br>

                    • EXAME DO PEDIDO – Apresentando o pedido será o mesmo submetido 
                    a exame formal preliminar e, se devidamente instruído, será protocolizado e
                    publicado na Revista da Propriedade Industrial para apresentação de 
                    oposição, no prazo de 60 (sessenta dias).</br></br>

                    • ACOMPANHAMENTO – O acompanhamento teve ser feito pela RPI, pois,
                    o INPI se manifesta através desta revista, é nela que saem às 
                    publicações, exigências, despachos etc.</br></br>

                    • CERTIFICADO – Após esta publicação, sua MARCA terá todas as vantagens,
                    dentre elas o direito de uso exclusivo do nome fantasia.</br></br>

                    <span class="Destaque">ETAPAS SIMPLIFICADAS DO TRAMITE PARA O REGISTRO DE MARCA:</span></br></br>

                    1. Depósito: a marca deverá ser publicada aproximadamente em 90 dias
                    contados da data do depósito;</br></br>

                    2. Publicação: até 60 (sessenta) dias após a data de publicação terceiros
                    poderão apresentar “oposição“;</br></br>

                    3. Oposição e manifestação: tendo sofrido oposição o detentor da marca terá
                    prazo de 60 (sessenta) dias a contar da publicação 
                    desta para se manifestar;</br></br>

                    4. Exame: o examinador poderá formular exigências a serem cumpridas.</br></br>

                    <span class="Destaque">FINALIZADO O EXAME, SERÁ PUBLICADO O DEFERIMENTO OU INDEFERIMENTO DO PEDIDO:</span></br></br>

                    1. Se deferido: deverá ser recolhida taxa de 
                    decênio (taxa válida para a proteção dospróximos 10 anos), e expedição de 
                    certificado (não é permitido apresentar recurso contra deferimento);</br></br>

                    2. Se indeferido: o titular poderá apresentar no prazo 
                    de 60 (sessenta) dias, recurso contra o indeferimento;</br></br>

                    3. Pagamento de decênio: deve-se recolher taxa de primeiro decênio e 
                    expedição de certificado de registro, para a marca deferida;</br></br>

                    4. Concessão: até 180 (cento e oitenta) dias da concessão poderá 
                    ser requerida a nulidade administrativa do Registro;</br></br>

                    5. Ação de nulidade: até cinco anos após a concessão da 
                    marca, poder-se-á iniciar ação de nulidade (esfera judicial).</br></br>

