                <h2>O que é marca?</h2>
                <p>
                Marca é todo sinal distintivo, visualmente perceptível, que identifica e distingue produtos e serviços, bem como certifica a conformidade dos mesmos com determinadas normas ou especificações técnicas.</br>
                A marca registrada garante ao seu proprietário o direito de uso exclusivo no território nacional em seu ramo de atividade econômica.</br>
                Ao mesmo tempo, sua percepção pelo consumidor pode resultar em agregação de valor aos produtos ou serviços.
                </p>

                <h2>Como registrar?</h2>
                <p>
                Em primeiro lugar, é aconselhável realizar uma busca prévia da marca para saber se já existe alguma marca depositada ou registrada (s) atividade de mercado (classe) pretendida.
                </p>

                <h2>A busca prévia é obrigatória?</h2>
                <p>
                A busca prévia de marca não é obrigatória, entretanto, é aconselhável ao interessado realizá-la antes de efetuar o depósito, na classe (atividade) que pretende registrar seu produto ou serviço, para verificar se já existe marca anteriormente depositada ou registrada.
                </p>

                <h2>O que é registrável como marca?</h2>
                <p>
                São registráveis como marca os sinais distintivos visualmente perceptíveis, não compreendidos nas proibições legais.</br>
                A marca pode ser para produto ou serviço para distinguir produto ou serviço de outro idêntico, semelhante ou afim.
                </p>

                <h2>O que não é registrável como marca?</h2>
                <p>
                Os sinais irregistráveis estão compreendidos no art. 124 da Lei da Propriedade Industrial. A Lei marcária brasileira não protege os sinais sonoros, gustativos e olfativos.</br>
                Também não são registradas como marca sinal de caráter genérico, quando tiver relação com o produto ou serviço a distinguir; reprodução ou imitação, no todo ou em parte, ainda que com acréscimo, e marca alheia registrada; letra, algarismo e data, isoladamente; e, expressão de propaganda.</br>
                </p>

                <h2>O que é marca nominativa?</h2>
                <p>
                É aquela constituída por uma ou mais palavras, ou combinação de letras e/ou algarismos, sem apresentação fantasiosa.
                </p>

                <h2>O que é marca figurativa?</h2>
                <p>
                É aquela constituída por desenho, figura ou qualquer forma estilizada de letra e número, isoladamente.
                </p>

                <h2>O que é marca mista?</h2>
                <p>
                É aquela constituída pela combinação de elementos nominativos e figurativos ou de elementos nominativos, cuja grafia se apresente de forma estilizada.
                </p>

                <h2>O que é marca tridimensional?</h2>
                <p>
                É aquela constituída pela forma plástica de produto ou de embalagem, cuja forma tenha capacidade distintiva em si mesma e esteja dissociada de qualquer efeito técnico.
                </p>

                <h2>O que é marca coletiva?</h2>
                <p>
                É aquela que visa identificar produtos ou serviços provindos de membros de uma determinada entidade.
                </p>

                <h2>O que é marca de certificação?</h2>
                <p>
                É aquela que atesta a conformidade de um produto ou serviço com determinadas normas ou especificações técnicas notadamente quanto à qualidade, natureza, material utilizado e metodologia empregada.
                </p>

                <h2>Quais são os direitos e deveres do titular de uma marca?</h2>
                <p>
                A marca registrada garante a propriedade e o uso exclusivo em todo o território nacional, por dez anos. O titular deve mantê-la em uso e prorrogá-la de dez em dez anos.
                </p>  
                
                <h2>Pessoa física pode requerer o registro?</h2>
                <p>
                A pessoa física pode requerer o registro de marca, desde que comprove a atividade exercida, através de documento comprobatório, expedido pelo órgão competente. Verifica-se a habilitação profissional diante do órgão ou entidade responsável pelo registro, inscrição ou cadastramento.
                </p>   

                <h2>Quando ocorre a perda do direito?</h2>
                <p>
                O registro da marca extingue-se pela expiração do prazo de vigência, pela renúncia (abandono voluntário do titular ou pelo representante legal), pela caducidade (falta de uso da marca) ou pela inobservância do disposto no art. 217 da LPI.
                </p>   
                
                <h2>Qual é o tempo de duração de um registro de marca?</h2>
                <p>
                O registro de marca vigorará pelo prazo de dez anos, contados da data da concessão do registro, prorrogáveis por períodos iguais e sucessivos. O pedido de prorrogação deverá ser formulado durante o último ano de vigência do registro, instruído com o comprovante do pagamento da respectiva retribuição.</br>
                Se o pedido de prorrogação não tiver sido efetuado até o termo final da vigência do registro, o titular poderá fazê-lo nos 6 (seis) meses subsequentes, mediante o pagamento de retribuição adicional.
                </p> 

                <h2>Qual é o sistema de registro de marca adotado pelo Brasil?</h2>
                <p>
                O sistema de registro de marca adotado pelo Brasil, é atributivo de direito, isto é, a sua propriedade e o seu uso exclusivo só são adquiridos pelo registro.
                </p>   

                <h2>O que é patente?</h2>
                <p>
                Patente é um título de propriedade temporária sobre uma invenção ou modelo de utilidade, outorgado pelo Estado aos inventores ou autores ou outras pessoas físicas ou jurídicas detentoras de direitos sobre a criação.</br>
                Com este direito, o inventor ou o detentor da patente tem o direito de impedir terceiros, sem o seu consentimento, de produzir, usar, colocar a venda, vender ou importar produto objeto de sua patente e/ ou processo ou produto obtido diretamente por processo por ele patenteado.</br>
                Em contrapartida, o inventor se obriga a revelar detalhadamente todo o conteúdo técnico da matéria protegida pela patente.
                </p>  

                <h2>Quais são os tipos de patentes e prazo de validade?</h2>
                <h2>Patente de Invenção (PI)</h2>
                <p>
                Produtos ou processos que atendam aos requisitos de atividade inventiva, novidade e aplicação industrial.
                Sua validade é de 20 anos a partir da data do depósito.
                </p>  
                <h2>Modelo de Utilidade (MU)</h2> 
                <p>
                Objeto de uso prático, ou parte deste, suscetível de aplicação industrial, que apresente nova forma ou disposição, envolvendo ato inventivo, que resulte em melhoria funcional no seu uso ou em sua fabricação.
                Sua validade é de 15 anos a partir da data do depósito.
                </p>  
                <h2>Certificado de Adição de Invenção (C)</h2> 
                <p>
                Aperfeiçoamento ou desenvolvimento introduzido no objeto da invenção, mesmo que destituído de atividade inventiva, porém ainda dentro do mesmo conceito inventivo.</br>
                O certificado será acessório à patente e com mesma data final de vigência desta.
                </p>  

                <h2>Posso patentear uma idéia?</h2>
                <p>
                Não. Em primeiro lugar, a Lei de Propriedade Industrial (LPI) exclui de proteção como invenção e como modelo de utilidade uma série de ações, criações, idéias abstratas, atividades intelectuais, descobertas científicas, métodos ou inventos que não possam ser industrializados.</br>
                Algumas destas criações podem ser protegidas pelo Direito Autoral, que nada tem a ver com o INPI.
                </p>  

                <h2>O que não pode ser patenteado?</h2>
                <p>
                - técnicas cirúrgicas ou terapêuticas aplicadas sobre o corpo humano ou animal;</br>
                - planos, esquemas ou técnicas comerciais de cálculos, de financiamento, de crédito, de sorteio, de especulação e propaganda;</br>
                - planos de assistência médica, de seguros, esquema de descontos em lojas e também os métodos de ensino, regras de jogo, plantas de arquitetura;</br>
                - obras de arte, músicas, livros e filmes, assim como apresentações de informações, tais como cartazes e etiquetas com o retrato do dono;</br>
                - idéias abstratas, descobertas científicas, métodos matemáticos ou inventos que não possam ser industrializados;</br>
                - o todo ou parte de seres vivos naturais e materiais biológicos encontrados na natureza, ou ainda que dela isolados, inclusive o genoma ou germoplasma de qualquer ser vivo natural e os processos biológicos naturais.
                </p>  

                <h2>Como proteger uma invenção ou criação industrializável?</h2>
                <p>
                Deve-se dar entrada no pedido junto ao INPI para proteger o invento. A Patente é o instrumento correto para isso. É necessário depositar um pedido no INPI o qual, depois de devidamente analisado, poderá se tornar uma Patente, com validade em todo o território nacional.
                </p>  

                <h2>Que documentos devo apresentar?</h2>
                <p>
                O pedido de patente deve ser acompanhado de um relatório descritivo, das reivindicações, de desenhos ,de um resumo e comprovante de pagamento da retribuição relativa ao depósito.</br>
                O relatório deve descrever o produto ou o processo para o qual se requer a proteção. A descrição deve ser feita de forma a permitir que uma pessoa especializada possa compreender e colocar em prática a tecnologia.</br>
                As reivindicações caracterizam as peculiaridades do invento para as quais se requer a proteção legal. São elas que estabelecem e delimitam os direitos da patente.</br>
                Os desenhos, quando necessários, têm a finalidade de completar a descrição, esclarecendo ou delimitando o conteúdo da invenção. O resumo deve ser uma descrição clara, objetiva e sucinta do objeto da patente.
                </p>  

                <h2>É necessário fazer uma pesquisa para saber se o invento já existe?</h2>
                <p>
                Antes de depositar o pedido de Patente, é recomendável que se faça primeiro uma busca para saber se não há nada igual ou semelhante já patenteado não somente em termos de Brasil, mas de mundo.
                </p>  

                <h2>Posso patentear um produto similar a outro que já está patenteado?</h2>
                <p>
                Não. A invenção não pode ser idêntica ou similar a uma já patenteada.
                </p>  

                <h2>A patente só tem validade no Brasil?</h2>
                <p>
                Sim, a patente é válida somente no território nacional.
                </p>  

                <h2>Qual a duração da Patente?</h2>
                <p>
                A Patente de Invenção vigorará pelo prazo de 20 (vinte) anos e a de Modelo de Utilidade pelo prazo de 15 (quinze) anos contados da data de depósito.
                </p>  

                <h2>Quais os direitos conferidos ao titular da Patente?</h2>
                <p>
                O titular da Patente tem o direito de impedir terceiros, sem o seu consentimento, de produzir, colocar à venda, usar, importar produto objeto da patente ou processo ou produto obtido diretamente por processo patenteado.</br>
                Terceiros podem fazer uso da invenção somente com a permissão do titular (licença).
                </p>  

                <h2>Uma vez feito o depósito da patente junto ao INPI, o requerente já poderá usufruir dos direitos de uma patente?</h2>
                <p>
                Não. O que o depositante possui é uma "expectativa de direito" que somente se confirmará caso venha a obter a patente.</br>
                Caso o depositante esteja sofrendo prejuízos por concorrência desleal de alguém que esteja produzindo o mesmo objeto de sua invenção, o depositante poderá contatar tal concorrente notificando-o de que, caso o concorrente insista na prática desleal ele poderá, quando obtiver a Carta-Patente, impetrar uma ação judicial de indenização por perdas e danos, que poderão ser contabilizados a partir da data de publicação da patente.</br>
                Neste caso, a publicação antecipada é útil para efeitos da determinação desta data.
                </p>  

                <h2>Existe o risco de que algum funcionário do INPI revele a terceiros a invenção ou modelo de um pedido em trâmite?</h2>
                <p>
                Não. Todos pedidos tramitam no mais estrito sigilo durante o período de dezoito meses (salvo pedido de publicação antecipada pelo depositante) até o período da publicação na Revista de Propriedade Industrial (RPI).
                </p>  

                <h2>Quais os benefícios para a sociedade do sistema de patentes?</h2>
                <p>
                Basicamente o sistema promove o progresso da técnica por dois motivos: ao constituir um incentivo ao inventor em prosseguir em suas pesquisas uma vez garantida a proteção aos investimentos realizados e em segundo lugar incentivando seus concorrentes a buscarem alternativas tecnológicas para conquistarem o mercado que não recorram de licenças de exploração de patentes.
                Com a divulgação da invenção pelo documento de patente, a sociedade se beneficia com o conhecimento de uma tecnologia que de outra forma permaneceria como segredo comercial.
                </p>  

